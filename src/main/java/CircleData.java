package main.java;

import java.awt.*;

public class CircleData {
    int x, y, radius, points;
    Color color;

    public CircleData(int x, int y, int radius, Color color, int points) {
        this.x = x;
        this.y = y;
        this.radius = radius;
        this.color = color;
        this.points = points;
    }
}
